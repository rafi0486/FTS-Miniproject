    private void InitUI(){
         ResultSet rs=con.getResultSet("select item_name FROM domain_items where d_master='app_status'");
        status.removeAllItems();
         try{
        while(rs.next()){
            status.addItem(rs.getString("item_name"));
   
        }
        }catch(Exception ex){
            
        }
    }
    private void LoadAppDetails(){
        app_id.setText(app_no+"");
        ResultSet rs=con.getResultSet("select app_title,app_desc,GetDateText(app_date) as app_date,GetUserAlias(submitted_by) submitted_by,GetDomainValue(status) as status,GetTypeName(app_type) as app_type from applications where app_id="+app_no+"");
        try{
        if(rs.next()){
            app_title.setText(rs.getString("app_title"));
            app_desc.setText(rs.getString("app_desc"));
            submitted_by.setText(rs.getString("submitted_by"));
            app_date.setText(rs.getString("app_date"));
            status.setSelectedItem(rs.getString("status"));
            app_type.setText(rs.getString("app_type")); 
            
             try{
             ResultSet rst=null;
            rst=con.getResultSet("select GetDateText(moved_on) as `Date`,GetDomainValue(action) as `Action`,GetUserAlias(from_user) as `From`,GetUserAlias(to_user) as `To`,remarks as `Remarks` from status_tracking where app_id="+app_no+" order by track_id desc");
             
            jtbl_log.setModel(con.buildTableModel(rst));
      }catch(Exception ex){
          
             System.err.println(ex+"");
      }   
        }
        }catch(Exception ex){
            
        }
    }

